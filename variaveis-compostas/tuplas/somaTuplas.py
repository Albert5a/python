a = (7, 1, 4)
b = (2, 7, 3, 8)
c = a + b
# Ordena Tuplas
print(sorted(c))
print(c)
# Conta elementos
print(c.count(7))
# Mostra o indice do elemtno
print(c.index(2))
# Mostra indice do elemento a partir do indice
print(c.index(7, 1))
